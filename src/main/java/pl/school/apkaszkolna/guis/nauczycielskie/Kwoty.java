package pl.school.apkaszkolna.guis.nauczycielskie;

public class Kwoty {

    private int h;
    private double w;

    public Kwoty(int h, int w) {
        this.h = h;
        this.w = w;
    }

    public Kwoty() {
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public double getW() {
        return w;
    }

    public void setW(double w) {
        this.w = w;
    }
}
